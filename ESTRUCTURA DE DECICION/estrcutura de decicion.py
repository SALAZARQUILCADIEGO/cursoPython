## estrructura de decision  if/elif/else 
## if realiza una accion especifica solo cuando la condicion es verdadera; de lo contrario, la accion se ignora.
## La estructura if/else permite al programador especificar que una accion diferente se debe desarrollar
## cuando la condicion es falsa.

## ejemplo
if nota >= 60:
   print ("Aprobado")
else:
      print ("Suspendido")
##Tambien se utiliza la estructura de seleccion multiple if/elif/else. Que se veria en Python de la siguiente manera:
